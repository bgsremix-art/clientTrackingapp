export const shareClientProgressPdf = async (params: {
  client: any;
  records: any[];
  attendance: any[];
  targetWeight: string;
  healthyMin: string;
  healthyMax: string;
  estimatedWeeks: string;
  labels: typeof defaultPdfLabels;
  branding?: Branding;
}) => {
  const { client, records, attendance, targetWeight, healthyMin, healthyMax, estimatedWeeks, labels, branding } = params;
  
  const recordsHtml = records.map(r => `
    <tr>
      <td>${formatDate(r.date, labels.dateLocale)}</td>
      <td>${r.currentWeightKG} kg</td>
      <td>${r.bmi.toFixed(1)}</td>
      <td>${escapeHtml(r.notes || '-')}</td>
    </tr>
  `).join('');

  const body = `
    <div class="profile-row">
      ${client.imageUri ? `<img src="${client.imageUri}" class="profile-img" />` : '<div class="profile-img"></div>'}
      <div>
        <div style="font-weight: bold; font-size: 18px;">${escapeHtml(client.name)}</div>
        <div class="muted">${escapeHtml(client.goal)} | ${client.heightCM} cm</div>
      </div>
    </div>

    <div class="section">
      <div class="section-title">${escapeHtml(labels.summary)}</div>
      <div class="highlight-box">
        <div class="metric">
          <div class="metric-label">Target Weight</div>
          <div class="metric-value">${targetWeight} kg</div>
        </div>
        <div class="metric">
          <div class="metric-label">Estimated Time</div>
          <div class="metric-value">${estimatedWeeks} weeks</div>
        </div>
        <div class="metric">
          <div class="metric-label">Healthy Range</div>
          <div class="metric-value">${healthyMin}-${healthyMax} kg</div>
        </div>
      </div>
    </div>

    <div class="section">
      <div class="section-title">History</div>
      <table>
        <thead>
          <tr>
            <th>${escapeHtml(labels.date)}</th>
            <th>${escapeHtml(labels.weight)}</th>
            <th>BMI</th>
            <th>Notes</th>
          </tr>
        </thead>
        <tbody>
          ${recordsHtml || `<tr><td colspan="4" style="text-align:center">No records found</td></tr>`}
        </tbody>
      </table>
    </div>
  `;

  const fileName = `ProgressReport_${client.name.replace(/\s+/g, '_')}.pdf`;
  await sharePdf(reportShell(labels.progressReport, body, labels, branding), fileName);
};

export const saveClientProgressPdf = async (params: any) => {
  await shareClientProgressPdf(params);
};

const createPdfFile = async (html: string, fileName: string) => {
  const { uri } = await Print.printToFileAsync({ html, base64: false });
  const namedUri = `${FileSystem.cacheDirectory}${fileName}`;
  await FileSystem.deleteAsync(namedUri, { idempotent: true });
  await FileSystem.copyAsync({ from: uri, to: namedUri });
  return namedUri;
};

const sharePdf = async (html: string, fileName: string) => {
  const uri = await createPdfFile(html, fileName);
  if (Platform.OS === 'ios') {
    await Sharing.shareAsync(uri, { UTI: 'com.adobe.pdf', mimeType: 'application/pdf' });
  } else {
    await Sharing.shareAsync(uri, { mimeType: 'application/pdf' });
  }
};


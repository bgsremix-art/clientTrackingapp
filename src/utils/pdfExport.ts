type Branding = {
  gymLogo?: string;
  gymName?: string;
  trainerName?: string;
};

const defaultPdfLabels = {
  generated: 'Generated',
  preparedFor: 'Prepared for',
  date: 'Date',
  generatedBy: 'Generated by ClientTracking',
  mealPlan: 'Meal Plan',
  progressReport: 'Progress Report',
  summary: 'Summary',
  change: 'Change',
  weight: 'Weight',
  category: 'Category',
  ingredient: 'Ingredient',
  portion: 'Portion',
  dailyTargets: 'Daily Targets',
  nutritionTarget: 'nutrition target',
  dateLocale: 'en-US',
};

const escapeHtml = (unsafe: string) => {
  return unsafe
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
};

const formatDate = (dateStr: string, locale = 'en-US') => {
  try {
    return new Date(dateStr).toLocaleDateString(locale, {
      year: 'numeric',
      month: 'long',
      day: 'numeric',
    });
  } catch (e) {
    return dateStr;
  }
};

export const getSafeFileName = (name: string, prefix: string) => {
  return `${prefix}_${name.replace(/[^a-z0-9]/gi, '_').toLowerCase()}_${Date.now()}`;
};

export const shareImageFile = async (uri: string, fileName: string) => {
  if (!(await Sharing.isAvailableAsync())) {
    throw new Error('Sharing is not available on this device');
  }
  const newPath = FileSystem.cacheDirectory + fileName;
  await FileSystem.copyAsync({ from: uri, to: newPath });
  await Sharing.shareAsync(newPath);
};

export const saveImageFile = async (uri: string, fileName: string) => {
  // Use the robust MediaLibrary-based saver for a better experience
  return await saveImageToGallery(uri);
};


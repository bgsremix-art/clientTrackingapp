const createPdfFile = async (html: string, fileName: string) => {
  const { uri } = await Print.printToFileAsync({ html, base64: false });
  const namedUri = `${FileSystem.cacheDirectory}${fileName}`;
  await FileSystem.deleteAsync(namedUri, { idempotent: true });
  await FileSystem.copyAsync({ from: uri, to: namedUri });
  return namedUri;
};

const savePdfToPhone = async (html: string, fileName: string) => {
  try {
    const namedUri = await createPdfFile(html, fileName);

    if (Platform.OS === 'android' && FileSystem.StorageAccessFramework) {
      const permissions = await FileSystem.StorageAccessFramework.requestDirectoryPermissionsAsync();
      if (!permissions.granted) return;

      const base64 = await FileSystem.readAsStringAsync(namedUri, { encoding: FileSystem.EncodingType.Base64 });
      const destinationUri = await FileSystem.StorageAccessFramework.createFileAsync(
        permissions.directoryUri,
        fileName,
        'application/pdf'
      );
      await FileSystem.writeAsStringAsync(destinationUri, base64, { encoding: FileSystem.EncodingType.Base64 });
      Alert.alert('Saved', `${fileName} saved to your selected folder.`);
      return;
    }

    const canShare = await Sharing.isAvailableAsync();
    if (canShare) {
      await Sharing.shareAsync(namedUri, {
        mimeType: 'application/pdf',
        dialogTitle: `Save ${fileName}`,
        UTI: 'com.adobe.pdf',
      });
      return;
    }

    Alert.alert('PDF created', `PDF saved at ${namedUri}`);
  } catch (error: any) {
    Alert.alert('PDF Error', error.message || 'Failed to save PDF.');
  }
};

const sharePdf = async (html: string, fileName: string) => {
  try {
    const namedUri = await createPdfFile(html, fileName);
    const canShare = await Sharing.isAvailableAsync();
    if (!canShare) {
      Alert.alert('PDF created', `PDF saved at ${namedUri}`);
      return;
    }
    await Sharing.shareAsync(namedUri, {
      mimeType: 'application/pdf',
      dialogTitle: fileName,
      UTI: 'com.adobe.pdf',
    });
  } catch (error: any) {
    Alert.alert('PDF Error', error.message || 'Failed to create PDF.');
  }
};

export const saveImageFile = async (uri: string, fileName: string) => {
  try {
    const namedUri = `${FileSystem.cacheDirectory}${fileName}`;
    await FileSystem.deleteAsync(namedUri, { idempotent: true });
    await FileSystem.copyAsync({ from: uri, to: namedUri });
    const { status } = await MediaLibrary.requestPermissionsAsync(true);
    if (status !== 'granted') {
      Alert.alert('Permission needed', 'Please allow photo access to save the image.');
      return;
    }
    await MediaLibrary.saveToLibraryAsync(namedUri);
    Alert.alert('Saved', `${fileName} saved to your photos.`);
  } catch (error: any) {
    Alert.alert('Image Error', error.message || 'Failed to save image.');
  }
};


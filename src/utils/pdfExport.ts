export const shareClientProgressPdf = async (params: {
  client: ProgressPdfClient;
  records: ProgressRecord[];
  attendance: AttendanceRecord[];
  targetWeight: number;
  healthyMin: number;
  healthyMax: number;
  estimatedWeeks: string;
  labels?: Partial<PdfLabels>;
}) => {
  const labels = getPdfLabels(params.labels);
  const latest = params.records[0];
  const attendedCount = params.attendance.filter(item => item.attended).length;
  const rows = params.records.slice(0, 20).map(record => `
    <tr>
      <td>${formatDate(record.date, labels.dateLocale)}</td>
      <td>${record.currentWeightKG} kg</td>
      <td>${record.bmi || calculateBMI(record.currentWeightKG, params.client.heightCM)}</td>
      <td>${escapeHtml(record.notes || '')}</td>
    </tr>
  `).join('');

  const body = `
    <h2>${escapeHtml(labels.reportSummary)}</h2>
    <div class="grid">
      <div class="box"><div class="label">${escapeHtml(labels.name)}</div><div class="value">${escapeHtml(params.client.name)}</div></div>
      <div class="box"><div class="label">${escapeHtml(labels.goal)}</div><div class="value">${escapeHtml(params.client.goal)}</div></div>
      <div class="box"><div class="label">${escapeHtml(labels.ageGender)}</div><div class="value">${params.client.age} / ${escapeHtml(params.client.gender)}</div></div>
      <div class="box"><div class="label">${escapeHtml(labels.height)}</div><div class="value">${params.client.heightCM} cm</div></div>
      <div class="box"><div class="label">${escapeHtml(labels.latestWeight)}</div><div class="value">${latest ? `${latest.currentWeightKG} kg` : 'N/A'}</div></div>
      <div class="box"><div class="label">${escapeHtml(labels.targetWeight)}</div><div class="value">${params.targetWeight} kg</div></div>
      <div class="box"><div class="label">${escapeHtml(labels.healthyRange)}</div><div class="value">${params.healthyMin} - ${params.healthyMax} kg</div></div>
      <div class="box"><div class="label">${escapeHtml(labels.estimatedTime)}</div><div class="value">${escapeHtml(params.estimatedWeeks)}</div></div>
    </div>

    <h2>${escapeHtml(labels.attendance)}</h2>
    <div class="box"><div class="label">${escapeHtml(labels.sessionsTracked)}</div><div class="value">${attendedCount}</div></div>

    <h2>${escapeHtml(labels.progressHistory)}</h2>
    <table>
      <tr><th>${escapeHtml(labels.date)}</th><th>${escapeHtml(labels.weight)}</th><th>BMI</th><th>${escapeHtml(labels.notes)}</th></tr>
      ${rows || `<tr><td colspan="4">${escapeHtml(labels.noProgressRecords)}</td></tr>`}
    </table>
  `;

  const fileName = `${getSafeFileName(params.client.name, 'progress-report')}.pdf`;
  await sharePdf(reportShell(labels.clientProgressReport, body, labels), fileName);
};


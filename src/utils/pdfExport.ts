export const getSafeFileName = (name?: string, suffix = 'report') => {
  const safeName = String(name || 'client')
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'client';
  const safeSuffix = suffix.replace(/[^a-z0-9-]+/gi, '-').replace(/^-+|-+$/g, '') || 'report';
  return `${safeName}-${safeSuffix}`;
};

